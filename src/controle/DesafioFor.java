public class DesafioFor {
	
	public static void main(String[] args) {
		String valor ="#";
		int a = 0;
		int e = 5;
		for( int i=a; i<=e; i++) {			
			System.out.println(valor);
			valor+="#";
		}
	


		
		for(String v="#"; !v.equals("######");v+="#" ) {			
		System.out.println(v);
		}
	}
}
